// Animated text component with typewriter effect
export function AnimatedText({ text, className, delay = 0 }: { text: string; className?: string; delay?: number }) {
  const [displayText, setDisplayText] = useState("");
  const [currentIndex, setCurrentIndex] = useState(0);
  const ref = useRef(null);
  const isInView = useInView(ref, { once: true });

  useEffect(() => {
    if (isInView && currentIndex < text.length) {
      const timer = setTimeout(() => {
        setDisplayText(text.slice(0, currentIndex + 1));
        setCurrentIndex(currentIndex + 1);
      }, 30 + delay);
      return () => clearTimeout(timer);
    }
  }, [isInView, currentIndex, text, delay]);

  return (
    <span ref={ref} className={className}>
      {displayText}
      {isInView && currentIndex < text.length && (
        <span className="animate-pulse text-violet-500">|</span>
      )}
    </span>
  );
}

// Animated section wrapper
export function AnimatedSection({ children, className, delay = 0 }: { 
  children: React.ReactNode; 
  className?: string; 
  delay?: number; 
}) {
  const ref = useRef(null);
  const isInView = useInView(ref, { once: true, margin: "-100px" });

  return (
    <motion.div
      ref={ref}
      initial={{ opacity: 0, y: 50 }}
      animate={isInView ? { opacity: 1, y: 0 } : { opacity: 0, y: 50 }}
      transition={{ duration: 0.6, delay, ease: [0.6, -0.05, 0.01, 0.99] }}
      className={className}
    >
      {children}
    </motion.div>
  );
}